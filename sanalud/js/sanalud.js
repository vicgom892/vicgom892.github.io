function abrir(){
    document.getElementById("menu1").style.width = "190px";
    document.getElementById("menu2").style.width = "170px";
    document.getElementById("menu3").style.width = "150px";
    
    
    document.getElementById("abrir").style.marginRight = "100px";
}

function cerrar(){
    document.getElementById("menu1").style.width = "0px";
    document.getElementById("menu2").style.width = "0px";
    document.getElementById("menu3").style.width = "0px";
    
    
    document.getElementById("abrir").style.marginRight = "50px";
}